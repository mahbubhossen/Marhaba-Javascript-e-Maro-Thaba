//2.
function kilowattToKiloCalori(kilowatt){
    const kilokalori = kilowatt * 860;
    return kilokalori;
}
console.log(kilowattToKiloCalori(5));

//3.
function hourToSecond(hour){
    const second = hour * 60 * 60;
    return second; 
}
console.log(hourToSecond(5));

